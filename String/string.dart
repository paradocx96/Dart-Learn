void main() {
  print('single quotes');

  final String statement = 'I\'m potato';
  print(statement);

  print("Double quotes");

  final statementUpdated = "I'm potato";
  print(statementUpdated);

  print("Mixed quotes");

  final mixedQuotes = 'Some statement with "Double quotes" in the middle.';
  print(mixedQuotes);
}
